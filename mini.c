void GLCD_init()
{
	PINSEL0 = 0; //set pins as GPIO
	PINSEL1 = 0;
	PINSEL2 = 0;

	 //set pins as output
	IODIR0 = (0XFF<<12);
	IODIR1 = (0X3F<<20);

	IOSET1 = (1<<24) | (1<<22) | (1<<23) ;
	IOCLR1 = (1<<25) | (1<<20) | (1<<21) ;

	GLCD_cmd(0x3F); //Display ON
	GLCD_cmd(0x40); //Select Column 0
	GLCD_cmd(0xB8);	//Select Page 0
}

